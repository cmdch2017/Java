package test_1023;

class StudentB{
	public StudentB(int no,String classNum,String name,String sex,int age){
		this.no=no;
		this.classNum=classNum;
		this.name=name;
		this.sex=sex;
		this.age=age;
	}
	@Override
	public String toString() {
		return "StudentB [no=" + no + ", classNum=" + classNum + ", name=" + name + ", sex=" + sex + ", age=" + age
				+ "]";
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getClassNum() {
		return classNum;
	}
	public void setClassNum(String classNum) {
		this.classNum = classNum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	int no;
	String classNum;
	String name;
	String sex;
	int age;
}
public class Demo2 {
	public static void main(String[] args) {
		StudentB s=new StudentB(10, "aa", "T", "ladies",20);
		System.out.println(s.toString());
	}
}
